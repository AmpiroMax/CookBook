#!/bin/bash

# Функция для записи рецепта в файл
write_recipe() {
    local file_path=$1
    local title=$2
    local description=$3
    local ingredients=$4
    local preparation=$5
    local servings=$6
    local calories=$7

    cat > "$file_path" <<EOL
# $title

## Описание
$description

## Ингредиенты
$ingredients

## Приготовление
$preparation

## Итог
- **Порции**: $servings
- **Калорийность**: $calories ккал
EOL
}


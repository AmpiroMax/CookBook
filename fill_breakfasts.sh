- Колбаса — 2 ломтика  
- Сыр — 2 ломтика" \
    "1. Выложить на хлеб колбасу и сыр.  
2. Поместить в микроволновку на 1–2 минуты.  
3. Дать немного остыть." \
    "1" \
    "250"

